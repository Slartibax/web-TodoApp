    {
        //TODO Не хватает валидации
//        return dd($request);
        $task = new Task(['name'=> $request->task_name,
            'description'=>$request->task_description,
            'schedule_date'=>$request->schedule_date,
            'project_id'=>$request->segment(3)
            ]);
        $task->save();
        return redirect()->route('dashboard.show',['project' => $request->project]);
    }

