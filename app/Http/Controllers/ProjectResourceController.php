     */
    public function store(Request $request)
    {
        $project = new Project(['name' => $request->project_name, 'owner_id' => $request->user()->id]);
        $project->save();
        $request->user()->projects()->attach($project->id);
        return redirect()->route('p.index',['project'=>$project->id]);
    }

